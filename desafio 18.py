import math
ang = float(input('me informe um ângulo: '))
ta = math.tan(math.radians(ang))
se = math.sin(math.radians(ang))
cos = math.cos(math.radians(ang))
print(f'de acordo com o ângulo informado, seu seno será {se:.2f}\nseu cosseno será {cos:.2f}\n e sua tangente será {ta:.2f}\n final do cálculo.')

